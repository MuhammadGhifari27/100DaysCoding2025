
package day1;
        public class Day93 {
    public static void main(String[] args) {

        double nilai1 = 4.2;
        double nilai2 = 4.8;

        System.out.println("=== Contoh Math.ceil() ===");
        System.out.println("ceil(4.2) = " + Math.ceil(nilai1));
        System.out.println("ceil(4.8) = " + Math.ceil(nilai2));

        System.out.println("\n=== Contoh Math.floor() ===");
        System.out.println("floor(4.2) = " + Math.floor(nilai1));
        System.out.println("floor(4.8) = " + Math.floor(nilai2));

        System.out.println("\n=== Contoh Math.round() ===");
        System.out.println("round(4.2) = " + Math.round(nilai1));
        System.out.println("round(4.8) = " + Math.round(nilai2));
    }
}
